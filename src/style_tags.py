def get_all_tags() -> dict:
    """返回完整标签库，供前端加载。"""
    return STYLE_TAGS


def get_all_presets() -> dict:
    """返回所有风格预设模板。"""
    return STYLE_PRESETS


def get_preset(name: str) -> Optional[dict]:
    """获取指定风格预设模板。"""
    return STYLE_PRESETS.get(name)


def merge_tags(selected_keys: list[str], category: str = None) -> str:
    """将选中的标签键合并为 Prompt 字符串。

    Args:
        selected_keys: 选中的标签 key 列表
        category: 可选过滤分类

    Returns:
        逗号分隔的 Prompt 风格标签字符串
    """
    if category:
        available = STYLE_TAGS.get(category, {}).get("tags", [])
        return ", ".join([t for t in selected_keys if t in available])

    result = []
    for cat_data in STYLE_TAGS.values():
        for tag in cat_data["tags"]:
            if tag in selected_keys:
                result.append(tag)
    return ", ".join(result)